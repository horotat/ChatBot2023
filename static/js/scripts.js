// Sign Up. After Sign up, user is redirected to GDPR and profile page 

$("form[name=signup_form").submit(function(e) {

    var $form = $(this);
    var $error = $form.find(".error");
    var data = $form.serialize();

    $.ajax({
        url: "/user/signup",
        type: "POST",
        data: data,
        dataType: "json",
        success: function(resp) {
            window.location.href = "/profile/edit"; 
        },
        error: function(resp) {
            console.log(resp);
            $error.routestext(resp.responseJSON.error).removeClass("error--hidden");
        }
    });

    e.preventDefault();
});

